# Define DSPy signature for evaluation
class EvaluateResponse(dspy.Signature):
    """Determine whether the context contains the information stated in the correct answer. Respond with 1 if yes, 0 if no."""

    context: str = dspy.InputField(desc="The context to evaluate")
    correct_answer: str = dspy.InputField(desc="The correct answer to check for")
    evaluation: int = dspy.OutputField(
        desc="1 if context contains the correct answer, 0 otherwise"
    )

# Create DSPy module for evaluation
class ResponseEvaluator(dspy.Module):
    def __init__(self):
        super().__init__()
        self.evaluate = dspy.ChainOfThought(EvaluateResponse)

    def forward(self, context, correct_answer):
        return self.evaluate(context=context, correct_answer=correct_answer)

class AnswerQuestion(dspy.Signature):
    """
    Given a piece of context and a question.
    Use the context to answer the question in 1 sentence.
    """

    context: str = dspy.InputField(desc="The context to evaluate")
    question: str = dspy.InputField(desc="The question to answer")
    answer: str = dspy.OutputField(desc="The answer to the question")

class EvaluateQA(dspy.Signature):
    """
    You are given a question, the correct answer to the question, and an answer to the question generated by an LLM.
    You must judge whether the LLMs answer to the question is correct based on how it matches up with the correct answer despite differences in wording that may happen.
    Return 1 if the llm answer is correct, 0.5 if the answer is partially correct or missing a detail, and 0 if the llm answer is wrong.
    """

    question: str = dspy.InputField(desc="The question that was asked")
    correct_answer: str = dspy.InputField(desc="The correct answer to the question")
    llm_answer: str = dspy.InputField(desc="The LLMs answer to the question that you must judge to see if it is correct")
    score: float = dspy.OutputField(desc="Your score for how correct or incorrect the LLM answer is")

class QAEvaluator(dspy.Module):
    def __init__(self):
        super().__init__()
        self.answer = dspy.ChainOfThought(AnswerQuestion)
        self.evaluator = dspy.ChainOfThought(EvaluateQA)

    def forward(self, question, context, correct_answer):
        answer = self.answer(context=context, question=question).answer
        score = self.evaluator(question=question,correct_answer=correct_answer,llm_answer=answer).score
        return score

def gpt_evaluate_response(correct_answer: str, context: str) -> int:
    evaluator = ResponseEvaluator()
    result = evaluator.forward(context=context, correct_answer=correct_answer)
    return result.evaluation

def evaluate_qa(question, context, correct_answer):
    evaluator = QAEvaluator()
    return evaluator.forward(question=question, context=context, correct_answer=correct_answer)